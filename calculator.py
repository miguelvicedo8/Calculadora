class Calculator():

    def suma(self, num1, num2):
        return num1 + num2

    def resta(self, num1, num2):
        return num1 - num2

    def multiplica(self, num1, num2):
        return num1 * num2

    def divide(self, num1, num2):
        return round(num1 / num2, 2)
